import { useState } from 'react'
import { usePredictions, useGeneratePredictions, useMembersSimple } from '../hooks/usePredictions'
import { TrendingUp, TrendingDown, RefreshCw, Sparkles } from 'lucide-react'
import StatusBadge from '@/components/StatusBadge'

export default function PredictionsPage() {
  const [selectedMemberId, setSelectedMemberId] = useState('')
  const [aiResults, setAiResults] = useState<any[] | null>(null)
  const [generating, setGenerating] = useState(false)

  const { data: predictions } = usePredictions()
  const { data: members } = useMembersSimple()
  const generateMutation = useGeneratePredictions()

  const generatePredictions = async () => {
    if (!selectedMemberId) return
    setGenerating(true)
    setAiResults(null)
    try {
      const data = await generateMutation.mutateAsync({ memberId: selectedMemberId, daysAhead: 30 })
      setAiResults(data)
    } catch (err) {
      alert(err instanceof Error ? err.message : 'Failed to generate predictions')
    } finally {
      setGenerating(false)
    }
  }

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-2xl font-bold text-[#ECECFC]">Predictive Analytics</h1>
        <p className="text-[#55557A] text-sm mt-1">
          AI-powered predictions for member progress and trends
        </p>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
        <div className="bg-[#14142A] p-6 rounded-xl border border-[#2A2A45] shadow-sm">
          <div className="flex items-center gap-2 text-sm text-[#55557A] mb-2">
            <TrendingUp className="w-4 h-4 text-[#22C55E]" />
            <span>Total Predictions</span>
          </div>
          <p className="text-2xl font-bold text-[#ECECFC]">{predictions?.length ?? 0}</p>
          <p className="text-xs text-[#55557A] mt-1">Generated by AI</p>
        </div>
        <div className="bg-[#14142A] p-6 rounded-xl border border-[#2A2A45] shadow-sm">
          <div className="flex items-center gap-2 text-sm text-[#55557A] mb-2">
            <Sparkles className="w-4 h-4 text-[#C084FC]" />
            <span>Generate New</span>
          </div>
          <div className="flex items-center gap-2">
            <select
              value={selectedMemberId}
              onChange={e => setSelectedMemberId(e.target.value)}
              className="flex-1 px-3 py-2 bg-[#1C1C35] border border-[#2A2A45] rounded-lg text-sm text-[#ECECFC] focus:outline-none focus:ring-2 focus:ring-[#7C3AED]/50"
            >
              <option value="">Select member...</option>
              {members?.map(m => (
                <option key={m.id} value={m.id}>{m.full_name}</option>
              ))}
            </select>
            <button
              onClick={generatePredictions}
              disabled={!selectedMemberId || generating}
              className="flex items-center gap-1 px-3 py-2 text-sm bg-gradient-to-r from-[#7C3AED] to-[#3B82F6] text-white rounded-lg hover:opacity-90 disabled:opacity-50 transition-opacity"
            >
              {generating ? <RefreshCw className="w-4 h-4 animate-spin" /> : <Sparkles className="w-4 h-4" />}
              {generating ? 'Generating...' : 'Generate'}
            </button>
          </div>
        </div>
        <div className="bg-[#14142A] p-6 rounded-xl border border-[#2A2A45] shadow-sm">
          <div className="flex items-center gap-2 text-sm text-[#55557A] mb-2">
            <TrendingDown className="w-4 h-4 text-[#F59E0B]" />
            <span>High Confidence</span>
          </div>
          <p className="text-2xl font-bold text-[#ECECFC]">
            {predictions?.filter(p => p.confidence >= 0.7).length ?? 0}
          </p>
          <p className="text-xs text-[#55557A] mt-1">Confidence &gt;= 70%</p>
        </div>
      </div>

      {aiResults && aiResults.length > 0 && (
        <div className="bg-gradient-to-r from-[#7C3AED]/10 to-[#3B82F6]/10 p-6 rounded-xl border border-[#7C3AED]/30 shadow-sm">
          <h2 className="text-lg font-semibold mb-4 text-[#ECECFC]">Latest AI Results</h2>
          <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
            {aiResults.map((r, i) => (
              <div key={i} className="bg-[#1C1C35] p-4 rounded-lg border border-[#2A2A45] shadow-sm">
                <p className="text-sm font-medium capitalize text-[#B4B4D0]">{String(r.prediction_type).replace(/_/g, ' ')}</p>
                <p className="text-2xl font-bold text-[#ECECFC] mt-1">{r.predicted_value}</p>
                <p className="text-xs text-[#55557A]">
                  Current: {r.current_value} {r.unit} &middot; {r.days_ahead}d ahead
                </p>
                <div className="mt-2">
                  <StatusBadge status={r.confidence >= 0.7 ? 'high' : r.confidence >= 0.4 ? 'medium' : 'low'} />
                </div>
              </div>
            ))}
          </div>
        </div>
      )}

      <div className="bg-[#14142A] rounded-xl border border-[#2A2A45] shadow-sm overflow-hidden">
        <div className="px-4 py-3 border-b border-[#2A2A45]">
          <h2 className="font-semibold text-[#ECECFC]">Recent Predictions</h2>
        </div>
        {predictions?.length === 0 ? (
          <div className="text-center py-8 text-[#55557A]">No predictions yet. Select a member and click Generate.</div>
        ) : (
          <table className="w-full">
            <thead>
              <tr className="border-b border-[#2A2A45] bg-[#0D0D1A]/50">
                <th className="text-left px-4 py-3 text-sm font-medium text-[#55557A]">Member</th>
                <th className="text-left px-4 py-3 text-sm font-medium text-[#55557A]">Metric</th>
                <th className="text-left px-4 py-3 text-sm font-medium text-[#55557A]">Predicted</th>
                <th className="text-left px-4 py-3 text-sm font-medium text-[#55557A]">Confidence</th>
              </tr>
            </thead>
            <tbody>
              {predictions?.map(p => (
                <tr key={p.id} className="border-b border-[#2A2A45]/50 last:border-0 hover:bg-[#7C3AED]/5 transition-colors">
                  <td className="px-4 py-3 text-sm font-medium text-[#ECECFC]">{p.profiles?.full_name}</td>
                  <td className="px-4 py-3 text-sm text-[#B4B4D0] capitalize">{String(p.metric_name).replace(/_/g, ' ')}</td>
                  <td className="px-4 py-3 text-sm text-[#B4B4D0]">{p.predicted_value}</td>
                  <td className="px-4 py-3">
                    <StatusBadge status={p.confidence >= 0.7 ? 'high' : p.confidence >= 0.4 ? 'medium' : 'low'} />
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        )}
      </div>
    </div>
  )
}
